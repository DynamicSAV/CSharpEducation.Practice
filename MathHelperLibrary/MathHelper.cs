namespace MathHelperLibrary;

public class MathHelper
{
  public double Add(float firstTerm, float secondTerm)
  {
    return firstTerm + secondTerm;
  }

  public double Substract(float diminutive, float deductible)
  {
    return diminutive - deductible;
  }

  public double Multiplication(float firstMultiplier, float secondMultiplier)
  {
    return firstMultiplier * secondMultiplier;
  }

  public double Divide(float divisible, float divider)
  {
    return divisible / divider;
  }
}